<h1>MCProxy</h1>
<h2>What's that for?</h2>

<h3>MCProxy is a "proxy" that allows you to download Minecraft versions from networks that have the Mojang servers blocked.</h3>
<h3>It's made primarily for the PolyMC and PrismLauncher launchers.</h3>

<h2>Note that this doesn't restore the authentification servers, it only allows you to download the versions jars/libs jars.</h2>
