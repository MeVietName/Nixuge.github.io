<h1>Open Media Grabber (Shortcut + Website)</h1>
A Media Grabber shortcut replica, but open source & using yt-dlp under the hood.<br>
Also has a public API.
<h3>Main software used:</h3>
<ul>
    <li>Vanilla JS, no framework (frontend)</li>
    <br>
    <li>Python 3.11 (backend)</li>
    <ul>
        <li>Flask (webserver)</li>
        <li>yt-dlp (downloading videos)</li>
    </ul>
</ul>
<h3>
    Note that even tho this software has been out for quite some time, it's
    still for me considered as beta due to its nature. Use at your own risks
    (even tho you don't really risk anything).
</h3>
