<h1 id="azspoof">AzSpoof (Minecraft Mod)</h1>
<p>A simple 1.9.4 Forge mod to make Funcraft &amp; other servers think you&#39;re on the AZ Launcher.</p>
<h1 id="what-are-funcraft-and-the-az-launcher">What are Funcraft and the AZ Launcher</h1>
<p>Funcraft is a (1.9.4) French server, and the AZ Launcher is their dedicated launcher. It&#39;s not needed to play on Funcraft, and indeed 1.9.0-&gt;1.11.2 clients work just about fine, but it&#39;s greatly pushed and can improve the gameplay experience a bit.</p>
<h1 id="what-can-you-do-with-the-az-launcher">What can you do with the AZ Launcher</h1>
<p>For most parts, it&#39;s just a normal Minecraft launcher like Lunar or Badlion, but this time in 1.9.4.</p>
<p>There are however some games (Rush 1vs1, Octogone) that can ONLY be played with that launcher for no apparent reason, apart from to push it unreasonably.</p>
<h1 id="why-not-play-on-it-then">Why not play on it then?</h1>
<ul>
<li>Limited set of mods, and like in some other launchers you can&#39;t add any more, so if you have a specific need you basically can&#39;t do much except playing on another launcher.</li>
<li>Sometimes buggy, not that buggy but has some quite annoying glitches while PVPing, which is basically the main point of using the launcher.</li>
<li>(my personal reason) Horrendous input issues on 1.9.4 (especially on Linux), which make the launcher basically unusable for anything against an even mid-good player.</li>
</ul>
<h1 id="why-release-that-mod-now-">Why release that mod now ?</h1>
<p>Had it for quite a while but basically just forgot about it, now that Funcraft is closing in a month (28 august 2023) might as well release it</p>
<h1 id="usage">Usage</h1>
<p>The mod is really primitive, it doesn&#39;t save any config or anything.</p>
<p>Use /azspoof to toggle on/off the mod (by default disabled when launching mc).</p>
<p>This mod is basically useless if not playing &quot;Octogone&quot; or &quot;Rush 1vs1&quot;</p>
