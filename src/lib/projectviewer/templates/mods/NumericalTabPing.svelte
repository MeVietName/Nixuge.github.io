<h1 id="numericaltabping">NumericalTabPing (Minecraft Mod)</h1>
<p>A simple mod that replaces the ping icon in tab with your actual ping as text</p>
<h2 id="compatible-versions">Compatible versions</h2>
<p>This mod has a version for 1.7.10 and one for 1.8.9.</p>
<p>Note that if using 1.8.9, Essentials also has that feature included by default.</p>
